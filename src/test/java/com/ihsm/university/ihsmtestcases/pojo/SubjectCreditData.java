package com.ihsm.university.ihsmtestcases.pojo;

public class SubjectCreditData {

	private String sessionField;
	private String batchField;
	private String academicPlanField;
	private String semField;
	private String subjectValue;

	// Getters and Setters
	public String getSessionField() {
		return sessionField;
	}

	public void setSessionField(String sessionField) {
		this.sessionField = sessionField;
	}

	public String getBatchField() {
		return batchField;
	}

	public void setBatchField(String batchField) {
		this.batchField = batchField;
	}

	public String getAcademicPlanField() {
		return academicPlanField;
	}

	public void setAcademicPlanField(String academicPlanField) {
		this.academicPlanField = academicPlanField;
	}

	public String getSemField() {
		return semField;
	}

	public void setSemField(String semField) {
		this.semField = semField;
	}

	public String getSubjectValue() {
		return subjectValue;
	}

	public void setSubjectValue(String subjectValue) {
		this.subjectValue = subjectValue;
	}

}
